import AppKit

/// Polls a pasteboard and forwards new text clips to a sink (the ClipStore).
/// Storage, dedupe, and capping live in ClipStore; this only detects changes and
/// filters out password/transient/empty copies. The pasteboard is injectable so
/// the filter logic is unit-testable without the user's real clipboard.
@MainActor
final class ClipboardMonitor {
    private let pasteboard: NSPasteboard
    private let shouldCapture: () -> Bool
    private let onCapture: (String) -> Void
    private let onCaptureImage: (Data) -> Void
    private var timer: Timer?
    private var lastChangeCount: Int

    /// Pasteboard markers we must never capture (passwords, transient, generated).
    private let ignoredTypes: Set<String> = [
        "org.nspasteboard.TransientType",
        "org.nspasteboard.ConcealedType",
        "org.nspasteboard.AutoGeneratedType",
        "org.nspasteboard.RestoredType"      // our own paste-backs (avoid a capture loop)
    ]

    init(pasteboard: NSPasteboard = .general,
         shouldCapture: @escaping () -> Bool = { true },
         onCapture: @escaping (String) -> Void,
         onCaptureImage: @escaping (Data) -> Void = { _ in }) {
        self.pasteboard = pasteboard
        self.shouldCapture = shouldCapture
        self.onCapture = onCapture
        self.onCaptureImage = onCaptureImage
        self.lastChangeCount = pasteboard.changeCount
    }

    func start() {
        guard timer == nil else { return }
        lastChangeCount = pasteboard.changeCount
        // Run in .common modes so polling keeps firing during tracking loops
        // (menu open, dragging the palette) instead of stalling in .default.
        let timer = Timer(timeInterval: 0.5, repeats: true) { [weak self] _ in
            // The timer is added to RunLoop.main, so it always fires on the main thread.
            MainActor.assumeIsolated { _ = self?.captureIfChanged() }
        }
        RunLoop.main.add(timer, forMode: .common)
        self.timer = timer
    }

    /// Detect a pasteboard change and forward the new text (if capturable).
    /// Returns true if a clip was forwarded. Internal for the timer + tests.
    @discardableResult
    func captureIfChanged() -> Bool {
        let changeCount = pasteboard.changeCount
        guard changeCount != lastChangeCount else { return false }

        // Read representations, then re-check changeCount: if it advanced mid-read,
        // the pasteboard was rewritten under us — discard and let the next tick
        // re-capture a stable snapshot (leave lastChangeCount untouched so we retry).
        let types = Set((pasteboard.types ?? []).map(\.rawValue))
        let text = pasteboard.string(forType: .string)
        let png = (text?.trimmingCharacters(in: .whitespacesAndNewlines).isEmpty ?? true) ? imagePNG() : nil
        guard pasteboard.changeCount == changeCount else { return false }

        lastChangeCount = changeCount

        guard shouldCapture() else { return false }   // pause / excluded app / password manager
        guard types.isDisjoint(with: ignoredTypes) else { return false }

        // Prefer text (more useful/searchable); fall back to an image copy when there's no text.
        if let text, !text.trimmingCharacters(in: .whitespacesAndNewlines).isEmpty {
            onCapture(text)
            return true
        }
        if let png {
            onCaptureImage(png)
            return true
        }
        return false
    }

    /// Normalize a pasteboard image to PNG bytes (so all image blobs share one compact format).
    private func imagePNG() -> Data? {
        if let png = pasteboard.data(forType: .png) { return png }
        guard let tiff = pasteboard.data(forType: .tiff), let rep = NSBitmapImageRep(data: tiff) else { return nil }
        return rep.representation(using: .png, properties: [:])
    }
}
